export function getLabel(key, lang = 'en') {
  const code = (lang || 'en').toLowerCase().split('-')[0];
  const dict = TRANSLATIONS[code] || TRANSLATIONS.en;
  return dict[key] || TRANSLATIONS.en[key] || key;
}

